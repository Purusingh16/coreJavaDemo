package arrayProgram;

public class Demo1 {

	public static void main(String[] args) 
	{
		String str[];
		str=new String[3];
		int size=str.length;
		for (int i = 0; i < size; i++) 
		{
			System.out.println(str[i]+"\t");
		}
		System.out.println("\n");
		int arr[];
		arr=new int[3];
		int size1=arr.length;
		for (int i = 0; i < size1; i++)
		{
			System.out.println(arr[i]+"\t");
		}
		
	}

}
